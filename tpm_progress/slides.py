def generate_slides(
    week: WeekData,
    output_dir: Path | None = None,
) -> Path:
    """Render WeekData to .pptx and return the saved path."""
    from pptx import Presentation
    from pptx.util import Inches

    output_dir = output_dir or DEFAULT_OUTPUT_DIR
    output_dir.mkdir(parents=True, exist_ok=True)

    prs = Presentation()
    prs.slide_width = Inches(13.33)   # 16:9
    prs.slide_height = Inches(7.5)

    _slide_title(prs, week)
    _slide_at_a_glance(prs, week)
    _slide_activity(prs, week)
    _slide_top_tasks(prs, week)
    _slide_quality(prs, week)
    _slide_artifacts(prs, week)
    _slide_lessons(prs, week)

    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    out_path = output_dir / f"weekly_progress_{week.week_end}_{timestamp}.pptx"
    prs.save(out_path)
    log.info("progress slides written: %s", out_path)
    return out_path
