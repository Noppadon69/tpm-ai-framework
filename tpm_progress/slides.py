"""
tpm_progress.slides - render WeekData into a 6-slide .pptx
ref: MASTER_PLAN_v5.md § 16.4

6 slides:
    1. Title cover (date range, intern name, week number)
    2. This week at a glance (counts + headline metrics)
    3. Top tasks completed (top-3 by duration, with subject + outcome)
    4. Quality + reliability (success rate, latency p90, egress, failures)
    5. Tools / artifacts produced (files + commits + libs)
    6. Lessons learned + next week (auditor findings + plan)
"""
from __future__ import annotations

import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from tpm_progress.data import WeekData

log = logging.getLogger(__name__)

REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_OUTPUT_DIR = REPO_ROOT / "output" / "progress_reports"

# Color palette - dark slate + accent (matches Chainlit dark theme)
COLOR_BG = (28, 30, 38)         # near black
COLOR_PRIMARY = (113, 152, 218)  # cool blue accent
COLOR_TEXT = (235, 237, 242)    # off-white
COLOR_DIM = (155, 162, 178)     # secondary text
COLOR_OK = (107, 191, 105)      # green
COLOR_WARN = (231, 165, 80)     # amber
COLOR_BAD = (218, 110, 100)     # soft red


# ============================================================
# Helpers
# ============================================================
def _add_text(slide, left, top, width, height, text: str, *,
              font_size=18, bold=False, color=COLOR_TEXT, align=None):
    from pptx.util import Inches, Pt
    from pptx.dml.color import RGBColor
    box = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
    tf = box.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    if align is not None:
        p.alignment = align
    run = p.add_run()
    run.text = text
    run.font.size = Pt(font_size)
    run.font.bold = bold
    run.font.color.rgb = RGBColor(*color)
    return box


def _add_bullet(slide, left, top, width, height, lines: list[str], *,
                font_size=14, color=COLOR_TEXT, bullet_color=COLOR_PRIMARY):
    from pptx.util import Inches, Pt
    from pptx.dml.color import RGBColor
    box = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
    tf = box.text_frame
    tf.word_wrap = True
    for i, line in enumerate(lines):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        bullet_run = p.add_run()
        bullet_run.text = "• "
        bullet_run.font.size = Pt(font_size + 2)
        bullet_run.font.color.rgb = RGBColor(*bullet_color)
        text_run = p.add_run()
        text_run.text = line
        text_run.font.size = Pt(font_size)
        text_run.font.color.rgb = RGBColor(*color)
    return box


def _set_slide_bg(slide, color):
    from pptx.dml.color import RGBColor
    fill = slide.background.fill
    fill.solid()
    fill.fore_color.rgb = RGBColor(*color)


def _fmt_int(n: int) -> str:
    return f"{n:,}"


def _fmt_seconds(ms: int) -> str:
    if ms <= 0:
        return "n/a"
    if ms < 1000:
        return f"{ms} ms"
    return f"{ms/1000:.1f} s"


